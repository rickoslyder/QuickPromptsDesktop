/**
 * Convert Material Design icon name to emoji
 * If no mapping exists, return a default emoji
 */
export const convertIconToEmoji = (iconName: string): string => {
  // If it's already an emoji (single character with unicode > 127), return as-is
  if (iconName.length <= 4 && /[\u{1F600}-\u{1F64F}]|[\u{1F300}-\u{1F5FF}]|[\u{1F680}-\u{1F6FF}]|[\u{1F1E0}-\u{1F1FF}]|[\u{2600}-\u{26FF}]|[\u{2700}-\u{27BF}]/u.test(iconName)) {
    return iconName;
  }
  
  // Convert Material Design icon name to emoji
  return iconNameToEmoji[iconName] || 'ðŸ“'; // Default to document emoji
};

/**
 * Check if a string is likely a Material Design icon name
 */
export const isMaterialIcon = (iconName: string): boolean => {
  return iconName.includes('_') || iconNameToEmoji.hasOwnProperty(iconName);
};